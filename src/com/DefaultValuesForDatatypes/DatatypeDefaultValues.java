package com.DefaultValuesForDatatypes;

public class DatatypeDefaultValues {

	// Class Level Variables....Global Level

	int a; // Declare a variable
	double b;

	long c;
	short d;

	byte k;
	char j;
	boolean status;

	public static void main(String[] args) {

		// Create an object

		DatatypeDefaultValues obj = new DatatypeDefaultValues();

		System.out.println(obj.a);

		System.out.println(obj.b);

		System.out.println(obj.c);

		System.out.println(obj.d);

		System.out.println(obj.k);

		System.out.println(obj.j);

		System.out.println(obj.status);
	}

}
